<p>You would think this is the worst thing I would want to hear. Certainly I would not want to read an article with that very headline. Well, there it is, and someone sent it to me.</p>

<p>I read it. The headline actually contradicts the advice given in the article.</p>

<blockquote class="blockquote"><p>Every so often someone will pitch a "Pokémon game for adults," and what follows will be the worst idea for a game you ever heard.</p></blockquote>

<p>What does Ms. Henley mean by "the worst idea"?</p>

<blockquote class="blockquote"><p>There will be suggestions that instead of battling jugglers and boy scouts, you should fight drug dealers and pimps. The series, which has long been built on love for your&nbsp;fellow creatures and a cutesy approach to combat, should become hyper violent and see Pokémon impaled on one another, their flesh ripped asunder in an orgy of blood and viscera. It should be set in dark alleyways overflowing with street crime, and Poison-types should be able to kill or mutate you if you get too close to them. These ideas are desperately uncreative and lacking any cohesion or purpose, &#8230;</p></blockquote>

<p>I agree completely. Those are terrible ideas. If you want to see stuff like that, you can find gory Pokémon fan fiction on the Internet. Or play Palworld. I'm certainly not interested in writing, creating or playing that.&nbsp;</p>

<blockquote class="blockquote"><p> &#8230; but that doesn't mean a Pokémon game for adults is inherently a terrible idea.</p></blockquote>

<p>Wait, are you saying that everything is <em>not necessarily wrong</em> about a Pokémon game for adults?</p>

<p>Ms. Henley observes that "the demand for an adult Pokémon game isn't going away." She mentions some examples of fangames built on Pokémon IP "which deliver versions of this ‘grown up game'" and says, "Still, these are inversions of the typical formula as opposed to growing it to tell a more mature tale."&nbsp;</p>

<p>Ms. Henley clarifies which formula we're inverting:</p>

<blockquote class="blockquote"><p>In Pokémon, we universally play as children and complete a drawn-out tutorial which not only supposes we have never played a Pokémon game before, it also positions our protagonist as a complete newbie. Then there's the structure, where we go from a nobody to the very best like no one ever was. It's an oddly childish ambition[.]</p></blockquote>

<p>Yes. That's a wonderful formula&mdash;for a game for children. So let's invert it. Let's tell stories of protagonists who are already competent monster trainers and take on challenge other than becoming the Best Monster Trainer Ever. For that matter, let's have some protagonists and antagonists who aren't even monster trainers. Since we are Americans, let's differentiate Monsters Masters &amp; Mobsters from the Japanese-made concepts and make it a uniquely American product. Let's draw inspiration from the United States and its many peoples, lands and history. Let's take tropes and put trainable monsters into them.</p>

<p>This is why we say "Pokémon is Monsters Masters &amp; Mobsters for children." We're inverting the formula and doing it proudly. </p>

<p>Ms. Henley concludes with this call to action:</p>

<blockquote class="blockquote"><p>A Pokémon game for adults sounds like a bad idea, but that's only because most of the ideas pushed to the forefront are bad themselves. A game that moves away from the stale structure and offers a more complex protagonist feels like it's worth trying, especially as the narrative grows in depth.&nbsp;</p></blockquote>

<p>Cool. Challenge accepted.</p>
