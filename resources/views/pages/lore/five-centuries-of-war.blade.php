<h1>The Five Centuries of War</h1>

<p>The Five Centuries of War are characterized by numerous pogroms against certain races, particularly those perceived as extraordinarily powerful.</p>

<p>Races with wings were particularly targeted for persecution, as people perceived them (both naturally and through propaganda campaigns) to be capable of conquering all of anthrope civilization.</p>

<p>The pogroms resulted in the extinction of several races and drove several other races into isolation.</p>


