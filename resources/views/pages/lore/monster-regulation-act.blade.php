    <h1>The Monster Regulation Act</h1>

<p>A massive package of laws restricting private use of monsters and delegating regulation of monsters to the Monster Sports Federation. </p>
<p>At over 2000 pages, it is by far the largest piece of legislation concerning monsters to come before any legislative body in anthropic history. </p>
<p>In fact it is one of the largest pieces of legislation ever considered by the National Legislature, comparable in size to the annual budget.</p>
<p>The primary author and sponsor is [[Evertrue Sweepstake]], a legislator with at least three decades of history with [[Amalica Switch]]. </p>
<p>Among the coauthors listed on the bill are Milo Sanjermaine, the current Chief Lobbyist of the Federation, and [[Burton Quiss]], the current Secretary and Chief Legal Officer.</p>
<p>The Act is a significant issue in the legislative elections of Year 61,88. Savvy candidates and political-action groups highlight the flaws of the act and turn public sentiment against it.</p>

