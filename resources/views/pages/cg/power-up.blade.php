<h1>Power Up</h1>

<p>When cards with certain statistics come onto the Battlefield, you can increase those statistics by doing one of the following:</p>

<ul>
    <li>Discard any number of cards from your hand.</li>
    <li>If playing the <a href="/cg/pdv">Personal-Deck Version</a>, discarding any number of cards from your Library (without looking at them).</li>
</ul>

The following statistics can be increased:

<ul>
    <li>Size: 1 unit per discarded card</li>
    <li>Speed: 1 unit per discarded card</li>
    <li>Health: 5 units per discarded card</li>
    <li>Level: 5 units per discarded card</li>
</ul>
