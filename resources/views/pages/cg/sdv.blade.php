<h1>Particular Rules for Single-Deck Play</h1>

<p>In the Single-Deck Version, all players draw from a common Library of 100 cards. This does <strong>not</strong> include the Monsters that are available for play. Do no tmix any unused Monster cards into the Library.</p>

<h3 id="setup-draft">Drafting Monsters</h3>
<p>Players may decide for themselves how each player will get which Monster. Options include:</p>
<ul>
<li>Put all Monsters face-up in the Battlefield and draft them, with one player at a time taking a Monster and putting it on your team.</li>
<li>Put the Monsters face-down in the Battlefield and choose them at random.</li>
</ul>
<p>Once players have their Monsters, take the remaining Monster cards and put them away from the Battlefield, such as back in the game box.</p>



