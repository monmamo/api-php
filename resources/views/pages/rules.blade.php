<li>Emoji may not be used as replacements for profanity or innuendo.</li>
</ul>

<h3>Do not encourage, glorify, or engage in self-destructive behavior or self-harm.</h3>
<ul>
<li>This includes sharing graphic content related to self-harm or discussing personal experiences in a way that may trigger others.</li>
</ul>

<h1>Prohibited Content</h1>
<ul>
<li>Images or videos showing or depicting exposure of human genitalia.</li>

<li>Graphic depictions of violence.</li>

<li>Promotions of products or services related to sex.</li>

<li>Discussion, promotion or engagement in any activity that is expressly prohibited by the laws of the United States and/or the country hosting the particular forum or venue.</li>

<li>Content that demeans or dehumanizes any human being or group of persons, particularly (but not limited to) biological sex, physical ability or lack thereof, mental health, neurodivergence, body shape or form, sexual orientation or preference, gender identity or dysphoria, ethnic heritage or identity, religious identity or lack thereof.</li>

<li>Profanity or vulgar talk that is deemed to be excessive (after a moderator has given a warning of such).</li>

<li>Personal and/or information of any member that the member has not shared publicly in the community, including (but not limited to) full legal name; any nickname not used in the forum or venue; employer; address, city, jurisdiction or country of residence or employment; personal telephone numbers and email addresses; names of immediate family, friends or associates.</li>

<li>Financial or banking information, such as credit card numbers or bank account information</li>

<li>Any attempt to solicit personal information from members for any purpose other than direct communication and collaboration.</li>

<li>Solicitation or begging for money, items, or services.</li>

<li>Any solicitation of sales that is not an approved advertisement.</li>

<li>Any political or religious solicitation, including solicitation of votes for particular candidates or measures; promotion of political parties; and religious proselytizing. (Respectful explanations of political views and religious beliefs are allowed, as long as such explanations do not segue into solicitation.)</li>

<li>Any threat of bodily harm or disclosure of personal information against any member.</li>

<li>Links to illegal or pirated content, such as copyrighted material, stolen software, and keys or serial numbers for software or games that have been obtained illegally.</li>

<li>Hacks, cheats, or other unauthorized tools or services.</li>

<li>Viruses or malicious software.</li>

<li>Counterfeit or fraudulent items, such as fake IDs or fraudulent financial documents in this server.</li>

<li>Recruiting or promotion of other servers or communities without prior approval from the server staff.</li>
</ul>

<h1>Enforcement</h1>
<ul>
<li>Enforcement of these rules helps maintain a positive and productive environment for all community members. Violations may result in content removal, warnings, or bans from the community at the moderators' discretion.</li>

<li>Moderators are allowed to remove, without warning, any content that threatens the safety of any member or may subject this community or Monsters Masters & Mobsters LLC to legal action.</li>

<li>Monsters Masters & Mobsters LLC reserves the right to pursue appropriate legal action to seek recompense from any person or entity whose actions subject the company to legal action.</li>
</ul>
