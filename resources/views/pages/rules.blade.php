<h1>Community Rules</h1>

<h3>Be respectful.</h3>
<ul>
<li>Treat other members with respect and courtesy.</li>
<li>Harassment, hate speech, discrimination, personal attacks, or demeaning language towards others will not be tolerated. When offering criticism, it should be constructive and aimed at helping improve, not belittling or disparaging others.</li>
<li>Discussions about politics, religion or controversial or sensitive topics may be terminated without warning if staff deems that the discussion is a detriment to the community.</li>
</ul>

<h3>Observe decorum.</h3>
<ul>
<li>Monsters Masters & Mobsters expects community members to speak and behave in a manner behooving adulthood.</li>
<li>Members are prohibited from posting certain content matter as listed below.</li>
<li>Members may use profanity and adult speech, but the community staff reserve the right to warn about excessive profanity or inappropriate speech and take appropriate action (including suspensions and bans) if such behavior persists.</li>
<li>Abbreviated, censored or coded profanity will be treated the same as explicit profanity.</li>
<li>While Monsters Masters & Mobsters is generally intended for adults, some situations and circumstances may be opened to younger audiences. In these cases, the staff reserve the right to impose additional or stricter rules on content and speech.</li>
</ul>

<h3>Stay on-topic.</h3>
<ul>
<li>Posts and discussions should be relevant to Monsters Masters & Mobsters and the particular topic or focus of the channel in which they are posted.</li>
</ul>

<h3>Respect and protect the privacy of others.</h3>
<ul>
<li>Do not share personal information about others without their consent. Sharing your own personal information is strongly discouraged. Staff may remove personal information of any sort, even if belonging to the person who posted it, without notice.</li>
</ul>

<h3>Give proper credit.</h3>
<ul>
<li>When sharing information or content from other sources, properly credit the original source.</li>
</ul>

<h3>No solicitations.</h3>

<ul>
<li>Community members are advised not to use the community to conduct sales or trades with other community members. Instead we encourage members who which to buy, sell or trade with others to use established marketplaces with safeguards for both buyer and seller.</li>
</ul>

<h3>Do not spam.</h3>
<ul>
<li>Do not send a lot of small messages right after each other. These disrupt the chat and make it hard to scroll through the server.</li>
<li>Please keep your messages at least 5 words long while chatting.</li>
<li>Please allow others to respond before making another post. (If at least five minutes post without a reply, you may make another relevant post.)</li>
</ul>

<h3>No offensive names and profile pictures.</h3>
<ul>
<li>These rules apply equally to offensive names and profile pictures.</li>
<li>Members who use offensive or inappropriate user names or profile pictures will be subject to immediate ban without warning.</li>
<li>Do not join voice chat channels without permission of the people already in there.</li>
<li>If you see that they have a free spot it is alright to join and ask whether they have an open spot, but leave if your presence is not wanted by whoever was there first.</li>
</ul>

<h3>Avoid false or misleading information.</h3>
<ul>
<li>Make sure to fact-check any information you share and only share reliable sources. Spreading false or misleading information can be harmful and undermines the trust and integrity of the community.Staff may remove any content that is intentionally misleading or falsely represents the actions, opinions and beliefs of any person.</li>
</ul>

<h3>Do not impersonate other users, staff members, or any other individuals or organizations.</h3>
<ul>
<li>This includes using similar usernames, profile pictures, or display names.</li>
</ul>

<h3>No excessive self-promotion.</h3>
<ul>
<li>Share your work in moderation, only in designated channels, and only as apropos to the topic at hand. Staff may remove any self-promotion that they deem inappropriate or excessive. Posting for the sole purpose of inappropriate self-promotion may result in an immediate ban.</li>
<li>Do not abuse server bots.</li>
<li>Only use server bots for their intended purposes and do not spam or misuse their commands.</li>
</ul>

<h3>Do not abuse emojis, capital letters or punctuation.</h3>
<ul>
<li>This can be disruptive and make the chat difficult to follow.</li>
<li>Individual posts may contain no more than three (3) emojis unless the emojis are necessary or prudent for conveying meaning and emotion.</li>
