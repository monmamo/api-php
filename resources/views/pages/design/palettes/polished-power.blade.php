    <h1>Color Scheme: Polished Power</h1>

<p>A color palette that reflects <strong>cold luxury, controlled magic, high-end opacity</strong>, and the eerie normalcy of elite systems.</p>

<x-color.table config="design.color.polished-power" />

<h2>Suggested Use of Palette</h2>
<dl class="row">
    <dt class="col-sm-3">Architecture</dt>
    <dd class="col-sm-9">Executive Steel, Archive Cream, Platinum Glare, Monotone Mint accents</dd>

    <dt class="col-sm-3">Corporate Logos</dt>
    <dd class="col-sm-9">Tinted Brass or Neon Azure on dark/neutral base</dd>

    <dt class="col-sm-3">Uniforms / Suits</dt>
    <dd class="col-sm-9">Authoritarian Ink, Executive Steel, Platinum Glare highlights</dd>

    <dt class="col-sm-3">Interior Spaces</dt>
    <dd class="col-sm-9">Archive Cream walls, Admin Frost lighting, Neon Azure displays</dd>

    <dt class="col-sm-3">Technology Interfaces</dt>
    <dd class="col-sm-9">Neon Azure text/icons on dark or Admin Frost backgrounds</dd>

    <dt class="col-sm-3">Official Signage / Seals</dt>
    <dd class="col-sm-9">Corporate Gold + Platinum on dark base</dd>

    <dt class="col-sm-3">Magic / Power Elements</dt>
    <dd class="col-sm-9">Use Neon Azure glow effects only in controlled or sanctioned contexts to suggest regulated power use</dd>
</dl>


<h2>Thematic Justification</h2>

<p>
    The <strong>"Polished Power"</strong> palette emphasizes <strong>composure, control, and a fear of unpredictability</strong>&mdash;exactly what high-functioning corporate or government entities value.
</p>
<p>
    There's a strong focus on <strong>cool and neutral tones</strong>, with <strong>metallic and neon accents</strong> representing technology and selective magical power.
</p>
<p>
    It reflects an environment where wealth is <em>restrained but evident</em>, where <strong>cleanliness and order</strong> are performative, and where <strong>aesthetic perfection</strong> hides deeper rot.
</p>


