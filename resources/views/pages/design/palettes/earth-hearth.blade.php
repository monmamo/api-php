    <h1>Color Scheme: Earth Hearth</h1>

    <p>This is a beautifully grounded and emotionally rich stratum of the __MonMaMo__ setting—the <strong>Second World</strong> is the heartland, the working class, the community-centered middle tier where <strong>effort, tradition, and togetherness</strong> shape the tone of life. It’s a world of safe homes and crowded rec centers, of weekends at monster matches, and cozy-but-worn furniture passed down with love.</p>

<h2>Themes</h2>

<ul>
    <li>Earned comfort.</li>
    <li>Generational warmth.</li>
    <li>Utilitarian beauty.</li>
    <li>Use of monsters as companions, beasts of burden, and instruments of safety and security.</li>
</ul>

<p>This palette is designed to evoke the warmth and comfort of home, while also reflecting the hardworking spirit of the Second World. It draws from:</p>

<ul>
    <li><strong>Natural tones</strong> found in the earth and plant life.</li>
    <li><strong>Domestic warmth</strong> of family and community spaces.</li>
    <li><strong>Faded industrial strength</strong> from the working-class aesthetic.</li>
    <li>Touches of color inspired by <strong>monster care, sport, and blue-collar pride</strong>.</li>
</ul>

