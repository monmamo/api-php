    <p>Monsters Masters &amp; Mobsters is a speculative fiction concept that incorporates elements of magical realism, professional sports and organized (and disorganized) crime. The world of Monsters Masters &amp; Mobsters is our world, but instead of humans there are two types of higher beings: monsters and anthropes. Monsters Masters &amp; Mobsters represents the belief in the power of imagination and the importance of storytelling. We value creativity, originality, and the ability of fantasy and speculative fiction to inspire, entertain, and bring people together.</p>

