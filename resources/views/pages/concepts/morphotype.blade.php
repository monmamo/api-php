<h1>morphotype</h1>

        <p>In biology, a <dfn>morphotype</dfn> refers to a group of organisms within a species that share distinct morphological characteristics, setting them apart from other groups within the same species. These differences can be in shape, size, color, or other structural features. Morphotypes are often used to describe variations that are not significant enough to warrant classification as separate species or subspecies. For example, in microbiology, bacterial strains within a single species that exhibit different morphological traits are considered different morphotypes.  </p>

<p>Monsters Masters & Mobsters uses morphotypes to classify monsters and anthropes based on their physical characteristics. The morphotype of a monster or an anthrope is determined by its symmetry, segmentation, and limb disposition. Monster and anthrope bodies have evolved to interact with their environments in ways that enhance survival and reproduction. </p>

<p>The following classes of morphotypes are mutually exclusive; that is, a monster can have only one of them:</p>
        <ul>
            <li><a href="/concepts/winged-morphotype">winged morphotype</a>: A morphotype that determines the size and shape of the wings of the monster or anthrope.</li>
            <li><a href="/concepts/posture-morphotype">posture morphotype</a>: A morphotype that determines the posture of the monster or anthrope.</li>
            <li><a href="/concepts/spinal-morphotype">spinal morphotype</a>: A morphotype based on what grows from the spine.</li>
            <li><a href="/concepts/body-covering-morphotype">body covering morphotype</a></li>
        </ul>
        

